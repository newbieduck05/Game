void Menuinitalize()
{
    for (int i = 0; i < 3; i++)
    {
        menu[i] = (Obj*)malloc(sizeof(Obj));
        menu[i]->x = 53;
        menu[i]->y = 55 + i * 2;
        menu[i]->color = WHITE;
    }

    menu[0]->shape = " [ N E W G A M E ] ";
    menu[1]->shape = "[ L O R D G A M E ]";
    menu[2]->shape = "    [ E X I T ]    ";


    currentMENU = NEW;
    menucontroler = (Obj*)malloc(sizeof(Obj));
    menucontroler->x = 50;
    menucontroler->y = 55;
    menucontroler->color = WHITE;
    menucontroler->shape = "▶";
}
void MenuProgress()
{
    if (GetAsyncKeyState(VK_RETURN))
    {
        switch (currentMENU)
        {
        case NEW:
            SetScene(STAGE);
            break;
        case LORD:
            break;
        case EXIT:
            break;
        default:
            break;
        }
    }

    if (GetAsyncKeyState(VK_UP))
    {
        if (menucontroler->y > menu[0]->y)
        {
            menucontroler->y -= 2;
            int menu = currentMENU;
            menu++;
            currentMENU = (Menu)menu;
        }
    }
    if (GetAsyncKeyState(VK_DOWN))
    {
        if (menucontroler->y < menu[2]->y)
        {
            menucontroler->y += 2;
            int menu = currentMENU;
            menu--;
            currentMENU = (Menu)menu;
        }
    }
}
