#pragma region Stage
void StageInitialize()
{
	player = (Obj*)malloc(sizeof(Obj));
	player->x = 6;
	player->y = 55;
	player->color = WHITE;
	player->shape[0] = "  ■  ";
	player->shape[1] = "■■■";
	player->shape[2] = " ■■ ";
}

void StageProgress()
{
	if (GetAsyncKeyState(VK_RETURN))
	{
		SetScene(LOGO);
	}

	if (GetAsyncKeyState(VK_LEFT))
	{
		player->x--;
		if (map[player->y][player->x] == 2)
		{
			player->x++;
		}
	}

	if (GetAsyncKeyState(VK_RIGHT))
	{
		player->x++;
		if (map[player->y][player->x] == 2)
		{
			player->x--;
		}
	}

	if (GetAsyncKeyState(VK_SPACE))
	{

	}
}
void StageRender()
{
	for (int y = 0; y < MAP_HEIGHT; y++)
	{
		for (int x = 0; x < MAP_WIDTH; x++)
		{
			switch (map[y][x])
			{
			case 0:
				break;
			case 1:
				MoveXY(x, y);
				ChangeColor(WHITE);
				printf("##");
				break;
			case 2:
				MoveXY(x, y);
				ChangeColor(WHITE);
				printf("");
				break;
			case 3:
				MoveXY(x, y);
				ChangeColor(WHITE);
				printf("■");
				break;
			}
		}
	}
	MoveXY(player->x, player->y);
	ChangeColor(player->color);
	printf(player->shape[3]);

}
void StageRelease()
{
	free(player);
}
