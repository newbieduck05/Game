{
    for (int i = 0; i < 32; i++)
    {
        MoveXY(23, 15 + i);
        ChangeColor(WHITE);
        printf(title[i]);
    }

    for (int i = 0; i < 3; i++)
    {
        MoveXY(menu[i]->x, menu[i]->y);
        ChangeColor(menu[i]->color);
        printf(menu[i]->shape);
    }

    MoveXY(menucontroler->x, menucontroler->y);
    ChangeColor(menucontroler->color);
    printf(menucontroler->shape);
}
