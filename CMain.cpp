void Play()
{
    switch (currentScene)
    {
    case LOGO:
        LogoProgress();
        LogoRender();
        break;
    case MENU:
        MenuProgress();
        MenuRender();
        break;
    case STAGE:
        StageProgress();
        StageRender();
        break;
    default:
        break;
    }
}
